# Creating a list
fruits = ["Apple", "Banana", "Orange"]
print(fruits)


#accessing elements in a list
fruits = ["Apple", "Banana", "Orange"]
print("First element:", fruits[0])
print("Second element:", fruits[1])
print("Last element:", fruits[-1])


#slicing
numbers = [10, 20, 30, 40, 50]
print(numbers[1:4])
print(numbers[:3])
print(numbers[2:])

#updating an element in a list 
fruits = ["Apple", "Banana", "Orange"]
fruits[1] = "Mango"
print(fruits)


